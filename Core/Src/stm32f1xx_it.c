/**
  * @brief This function handles EXTI line[9:5] interrupts.
  */
void EXTI9_5_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI9_5_IRQn 0 */

  /* USER CODE END EXTI9_5_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_5);
  /* USER CODE BEGIN EXTI9_5_IRQn 1 */

  if (cur_menu_pos)
	  change_value = !change_value;

  //cur_menu_pos++;
  if (change_value)
  {
		TIM4->CNT = 0;

		switch (cur_menu_pos)
		{
			case 2:
			{
				TIM4->CNT = out_I % 10 * 2;
				break;
			}
			case 3:
			{
				TIM4->CNT = out_I % 100 / 10 * 2;
				break;
			}
			case 4:
			{
				TIM4->CNT = out_I / 100 * 2;
				break;
			}

			case 5:
			{
				TIM4->CNT = minimum_U % 10 * 2;
				break;
			}
			case 6:
			{
				TIM4->CNT = minimum_U % 100 / 10 * 2;
				break;
			}
			case 7:
			{
				TIM4->CNT = minimum_U / 100 * 2;
				break;
			}

		}
  }
  else
  {
	  TIM4->CNT = cur_menu_pos * 2;
  }

  /* USER CODE END EXTI9_5_IRQn 1 */
}

